export const TETROMINO_COLORS: Record<TetrominoType, string> = {
  I: '#00f0f0', // Cyan
  O: '#f0f000', // Yellow
  T: '#a000f0', // Purple
  S: '#00f000', // Green
  Z: '#f00000', // Red
  L: '#f0a000', // Orange
  J: '#0000f0', // Blue
};

export const TETROMINO_TYPES: TetrominoType[] = ['I', 'O', 'T', 'S', 'Z', 'L', 'J'];

export function getRandomTetromino(): TetrominoType {
  return TETROMINO_TYPES[Math.floor(Math.random() * TETROMINO_TYPES.length)];
}

export function createTetromino(type: TetrominoType, boardWidth: number): import('./types').Tetromino {
  const shape = TETROMINO_SHAPES[type][0];
  return {
    type,
    shape,
    rotation: 0,
    position: {
      x: Math.floor((boardWidth - shape[0].length) / 2),
      y: 0,
    },
  };
}
